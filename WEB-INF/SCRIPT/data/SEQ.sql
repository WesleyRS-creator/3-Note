CREATE SEQUENCE SEQ_MATIERE_ID
START WITH 1
INCREMENT BY 1
NOCACHE;

CREATE OR REPLACE TRIGGER TRG_MATIERE_ID
BEFORE INSERT ON MATIERES
FOR EACH ROW
BEGIN
    IF :NEW.ID_MATIERE IS NULL THEN
        :NEW.ID_MATIERE := SEQ_MATIERE_ID.NEXTVAL;
    END IF;
END;
/


